/* =========================================================
   I18N ENGINE
   - Reads data-i18n (textContent) and data-i18n-html (innerHTML)
   - Persists language choice in localStorage
   - Toggles <html lang=""> attribute
   ========================================================= */
(function () {
  var STORAGE_KEY = 'atlas_lang';
  var current = localStorage.getItem(STORAGE_KEY) || 'en';

  function apply(lang) {
    var dict = window.ATLAS_I18N[lang];
    if (!dict) return;
    current = lang;
    localStorage.setItem(STORAGE_KEY, lang);
    document.documentElement.lang = lang;

    /* Plain text */
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      if (dict[key] !== undefined) el.textContent = dict[key];
    });

    /* HTML content (headings with <em>, lists with <strong>, etc.) */
    document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-html');
      if (dict[key] !== undefined) el.innerHTML = dict[key];
    });

    /* Placeholder attributes */
    document.querySelectorAll('[data-i18n-ph]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-ph');
      if (dict[key] !== undefined) el.placeholder = dict[key];
    });

    /* Update toggle button state */
    var toggle = document.getElementById('langToggle');
    if (toggle) {
      toggle.setAttribute('data-lang', lang);
      var enSpan = toggle.querySelector('.lt-en');
      var idSpan = toggle.querySelector('.lt-id');
      if (enSpan) enSpan.classList.toggle('lt-active', lang === 'en');
      if (idSpan) idSpan.classList.toggle('lt-active', lang === 'id');
    }
  }

  /* Wire up toggle button (may be called before DOM ready) */
  function wireToggle() {
    var toggle = document.getElementById('langToggle');
    if (!toggle) return;
    toggle.addEventListener('click', function () {
      apply(current === 'en' ? 'id' : 'en');
    });
  }

  document.addEventListener('DOMContentLoaded', function () {
    wireToggle();
    apply(current);
  });

  /* Expose for external use */
  window.atlasSetLang = apply;
})();
